/* Эта реализация за O(N log N), сдать можно, например, на: https://informatics.msk.ru/mod/statements/view.php?chapterid=113287#1

Чтобы сделать из $O(N^2)$ заветные $O(N \log N)$, мы оставляем внешнюю сканирующую прямую (которая идет слева направо по координате $X$),
а вот внутренний цикл (который бежал по $Y$) заменяем на Дерево Отрезков. Так как координаты $Y$ могут быть до $10^9$, мы не можем построить
ДО на них напрямую. Поэтому мы применяем сжатие координат: собираем все уникальные координаты $Y$ в отсортированный массив и строим дерево
не по самим координатам, а по их индексам (где каждый лист дерева отвечает за отрезок между двумя соседними $Y$). Самая красивая фишка
этой задачи: в ДО нам не нужно проталкивание (lazy propagation)! Так как мы всегда удаляем ровно те же отрезки, которые добавляли,
мы можем пересчитывать длину покрытия просто по значениям детей, не спуская обновления вниз.
Данное решение получает OK и затрачивает максимум 0.38 секунд (время) и 25530368 байт (памятть) на N до 10^5 прямоугольников с координатами по модулю до 10^9.

P.S. Немного о сжатом дереве отрезков. Тут нужно понимать, что сжимаем мы точки, но дерево всё равно строится на интервалах.

Нарисуем пример. Пусть у нас есть уникальные Y-координаты после сжатия: **10, 20, 30, 40**.
Массив $Y$ выглядит так: 
* `Y[0] = 10`
* `Y[1] = 20`
* `Y[2] = 30`
* `Y[3] = 40`

Если мы построим ДО на самих **точках** (индексах 0, 1, 2, 3), то лист `0` будет отвечать за точку 10. Какая у нее длина? Ноль. Точка не имеет площади.
Прямоугольники существуют не в точках, а между ними! Поэтому мы должны построить ДО на элементарных ("элементарынх" в том смысле, что между
ними ничего интересного нам нет -- мы взяли все y-координаты из задачи, отсортировали и пронумеровали... между соседними точками
новых y нет!) интервалах между точками.

Вот как мы назначаем индексы ДО (листья) этим промежуткам:
* **Лист 0:** интервал между `Y[0]` и `Y[1]` (от 10 до 20).
* **Лист 1:** интервал между `Y[1]` и `Y[2]` (от 20 до 30).
* **Лист 2:** интервал между `Y[2]` и `Y[3]` (от 30 до 40).

Всего у нас 4 точки, но только **3 интервала** (с индексами от 0 до 2). 

1. Как обновлять?
Пусть пришел прямоугольник с границами от 10 до 30.
* Его $idx1$ = 0 (потому что 10 это `Y[0]`).
* Его $idx2$ = 2 (потому что 30 это `Y[2]`).
Какие интервалы он накрывает физически? Он накрывает интервал 10-20 (**Лист 0**) и интервал 20-30 (**Лист 1**). 
Значит, нам нужно обновить в ДО листья с `0` по `1`. То есть от $idx1$ до $idx2 - 1$.
Поэтому при обновлении прямоугольника на точках [idx1, idx2] мы в ДО обновляем интервал [idx1, idx2-1].

2. Чем равна длина узла?
Допустим, какой-то узел ДО покрывает отрезки наших листьев с $tl = 0$ до $tr = 1$ (оба включительно, у нас отрезок с обоими концами). 
Какую физическую длину покрывает этот узел? Он покрывает Лист 0 и Лист 1, то есть пространство от 10 до 30. 
* Координата начала — это `Y[0]` (то есть `Y[tl]`).
* Координата конца — это `Y[2]` (то есть `Y[tr + 1]`).
Длина равна `30 - 10 = 20`. Формулой это записывается ровно как `Y[tr + 1] - Y[tl]`. Поэтому длина узла [tl, tr] равна `Y[tr + 1] - Y[tl]`.

3. Кстати, в данной реализации в функции update при спуске по дереву мы пишем `l == tl && r == tr`, а не `l <= tl && r <= tr`.
Можно и более привычный второй вариант, но здесь мы искусственно «обрезаем» границы запроса `l` и `r` так, чтобы они никогда не
выходили за границы текущего узла (min(r, tm) и max(l, tm + 1)) оскольку мы обрезаем `l` и `r`, в тот момент, когда запрос полностью накрывает
узел, переменные `l` и `r` будут **в точности равны** `tl` и `tr`.
*/

using namespace std;

// Структура для вертикальных сторон прямоугольника (событий)
struct Event {
    int x;       // x-координата вертикальной стороны
    int y1, y2;  // нижняя и верхняя y-координаты стороны
    int type;    // 1 — левая граница (открываем), -1 — правая (закрываем)
    
    Event(int x, int y1, int y2, int type) : x(x), y1(y1), y2(y2), type(type) {}
    
    bool operator<(const Event& other) const {
        // Сортируем события слева направо по X.
        // Если координаты совпадают, сначала обрабатываем закрывающие, потом открывающие.
        return x < other.x; 
    }
};

// Узел Дерева Отрезков
struct Node {
    int cov = 0;         // Сколько прямоугольников сейчас ПОЛНОСТЬЮ покрывают этот отрезок
    long long len = 0;   // Фактическая длина объединения (в координатах Y) на этом отрезке
};

vector<Node> tree;       // Само дерево отрезков: по индексу 1 храним корень, у вершины v дети 2v и 2v+1
vector<int> Y;           // Массив сжатых уникальных Y-координат

// Функция обновления ДО на отрезке [l, r] (в индексах массива Y) (прибавляем val на отрезке)
// tl, tr - границы текущего узла ДО, этот узел хранится в tree по индексу v
void update(int v, int tl, int tr, int l, int r, int val) {
    if (l > r) return;                 // Некорректный запрос — выходим
    
    if (l == tl && r == tr) {          // Если отрезок запроса полностью совпал с отрезком узла
        tree[v].cov += val;            // Увеличиваем/уменьшаем счетчик покрытия
    } else {                           // Иначе бьем запрос на две части (идем в детей)
        int tm = tl + (tr - tl) / 2;
        update(v * 2, tl, tm, l, min(r, tm), val);
        update(v * 2 + 1, tm + 1, tr, max(l, tm + 1), r, val);
    }
    
    // ПЕРЕСЧЕТ длины (len) для текущего узла v:
    if (tree[v].cov > 0) {
        // Если узел полностью накрыт хотя бы одним прямоугольником, 
        // его длина равна физической длине всего отрезка [tl, tr] в координатах Y.
        // Заметим: tr — это индекс начала последнего элементарного отрезка,
        // поэтому его конец лежит в Y[tr + 1].
        tree[v].len = Y[tr + 1] - Y[tl]; 
    } else {
        // Если он не накрыт полностью, его длина собирается из длин детей.
        if (tl == tr) {                // Если это лист (детей нет)
            tree[v].len = 0;           // длина покрытия = 0 (физически, конечно, лист отвечает за элементарный отрезок Y[tl+1]-Y[tl], но так как он полностью
                                       // не накрыт, то он не накрыт вообще (это же элементарный отрезок -- внутри других y нет, его нельзя накрыть частично))
        } else {                       // Если есть дети
            tree[v].len = tree[v * 2].len + tree[v * 2 + 1].len;
        }
    }
}

int main() {
    ios_base::sync_with_stdio(false);  // Оптимизация ввода-вывода
    cin.tie(NULL);
    
    int N;
    cin >> N;
    if (N == 0) {
        cout << 0 << endl;
        return 0;
    }
    
    vector<Event> events;              // Массив событий
    
    for (int i = 0; i < N; i++) {
        int x1, y1, x2, y2;
        cin >> x1 >> y1 >> x2 >> y2;
        
        events.emplace_back(x1, y1, y2, 1);   // Левая сторона (вход)
        events.emplace_back(x2, y1, y2, -1);  // Правая сторона (выход)
        
        Y.push_back(y1);                    // Сохраняем y-координаты для сжатия
        Y.push_back(y2);
    }
    
    // СЖАТИЕ КООРДИНАТ:
    sort(Y.begin(), Y.end());                 // Сортируем все Y по возрастанию
    Y.erase(unique(Y.begin(), Y.end()), Y.end()); // Удаляем дубликаты
    
    int M = Y.size();                         // Количество уникальных координат
    // Если есть M точек, то между ними M - 1 элементарных отрезков.
    // Дерево отрезков строим на этих (M - 1) элементарных интервалах: от 0 до M - 2.
    tree.resize(4 * M);  // но памяти с запасом выделяем...
    
    sort(events.begin(), events.end());       // Сортируем события по X
    
    long long total_area = 0;                 // Итоговая площадь
    int prev_x = events[0].x;                 // X-координата предыдущего события
    
    for (int i = 0; i < events.size(); i++) {
        // 1. Прибавляем к площади: (текущий X - прошлый X) * (текущая длина открытых Y)
        // tree[1].len всегда хранит общую длину объединения по Y для текущего "среза"
        total_area += 1LL * (events[i].x - prev_x) * tree[1].len;
        
        // 2. Находим сжатые индексы для [y1, y2]
        // lower_bound возвращает итератор, дистанция от начала — это и есть индекс
        int idx1 = lower_bound(Y.begin(), Y.end(), events[i].y1) - Y.begin();
        int idx2 = lower_bound(Y.begin(), Y.end(), events[i].y2) - Y.begin();
        
        // 3. Обновляем Дерево Отрезков (начиная с корня: его индекс=1 и покрывает он все отрезки - от 0 до M-2)
        // Мы добавляем/удаляем интервал [y1, y2], что в индексах соответствует [idx1, idx2 - 1]
        if (idx1 <= idx2 - 1) {
            update(1, 0, M - 2, idx1, idx2 - 1, events[i].type);
        }
        
        prev_x = events[i].x;                 // Обновляем координату для следующего шага
    }
    
    cout << total_area << "\n";
    
    return 0;
}
