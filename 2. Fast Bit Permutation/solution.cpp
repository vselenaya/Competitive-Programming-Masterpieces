#include <iostream>
#include <vector>
#include <cstdint>

using namespace std;

/*
 * =======================================================================================
 * ПОДРОБНОЕ ОПИСАНИЕ РЕШЕНИЯ: ОПТИМИЗАЦИЯ ПЕРЕСТАНОВКИ БИТОВ (Meet-in-the-Middle)
 * =======================================================================================
 * * === 1. ОПЕЧАТКА В УСЛОВИИ ЗАДАЧИ ===
 * В условии сказано, что после перестановки получается число y, где y_0 = x_{p_0}, 
 * ..., y_{31} = x_{p_{31}}. То есть бит на позиции p_i из исходного числа встает 
 * на позицию i в новом. 
 * НА САМОМ ДЕЛЕ: Тесты задачи предполагают обратную логику. Мы считаем, что i-й бит 
 * исходного числа x отправляется на позицию p[i] в новом числе y. 
 * То есть y_{p_i} = x_i. Мы будем реализовывать именно эту логику.
 * * === 2. ПРОБЛЕМА: СКОРОСТЬ ===
 * Нам нужно сгенерировать n = 10^8 чисел и для каждого сделать перестановку 32 бит.
 * Если делать это в лоб (пробегаясь циклом от 0 до 31), мы получим ~3.2 * 10^9 операций. 
 * Это слишком медленно и не уложится в стандартные лимиты времени.
 * * === 3. ИДЕЯ РЕШЕНИЯ: РАЗДЕЛЯЙ И ВЛАСТВУЙ (ПРЕДПОДСЧЕТ) ===
 * Что если мы заранее посчитаем, куда переходят биты? Запомнить ответы для всех возможных
 * 32-битных чисел невозможно (нужен массив на 4 миллиарда элементов, что съест гигабайты памяти).
 * Но мы можем разбить 32-битное число на две половинки по 16 бит! Для 16 бит существует всего
 * 2^16 = 65536 возможных значений. Мы можем завести два массива (для младшей и старшей половин чисел),
 * и для каждой из 65536 комбинаций ОДИН РАЗ честно посчитать, в какое 32-битное число она превратится 
 * после применения нашей перестановки p.
 * * === 4. ПОЧЕМУ ЭТО РАБОТАЕТ (ЛИНЕЙНОСТЬ) ===
 * Перестановка бит p(x) дистрибутивна относительно побитового ИЛИ: p(a | b) = p(a) | p(b)
 * Это позволяет рассматривать 32-битное число x как сумму двух независимых 32-ух битных масок:
 * 1. Установлены старшие 16 бит (остальные младшие 16 забиты нулями), то есть это число = x >> 16.
 * 2. Младшие 16 бит установлены (остальные старшие 16 занулены), это число x & 0xFFFF.
 * И если самих вариантов числа x ровно 2^32 штук, то таких его масок-половинок может быть по 2^16
 * (так как всего 16 бит установлено в каждой половинке, оставшиеся всегда равны 0).
 * * === 5. ГЕНЕРАЦИЯ И СУММИРОВАНИЕ ===
 * Теперь генерация очередного числа выглядит так:
 * 1. Получаем новое s через линейный конгруэнтный генератор: s = s * a + c
 * 2. Берем младшие 16 бит (s & 0xFFFF) и смотрим их маску в массиве lo.
 * 3. Берем старшие 16 бит (s >> 16) и смотрим их маску в массиве hi.
 * 4. Так как исходные биты не пересекались, их итоговые позиции тоже не пересекутся. 
 *    Значит, мы можем просто сложить эти две маски через побитовое ИЛИ (|) или ИСКЛЮЧАЮЩЕЕ ИЛИ (^).
 * 5. Прибавляем результат к общей сумме (в 64-битном типе, чтобы избежать переполнения).
 * =======================================================================================
 */

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    uint32_t a, c, s;
    cin >> n >> a >> c >> s;

    vector<int> p(32);
    for (int k = 0; k < 32; k++) {
        cin >> p[k];
    }

    // lo: массив предвычисленных значений для младших 16 бит
    vector<uint32_t> lo(1 << 16, 0);
    for (int x = 0; x < (1 << 16); x++) {
        uint32_t y = 0;
        for (int k = 0; k < 16; k++) {
            if ((x >> k) & 1) {     // если k-ый бит в x установлен,
                y |= (1u << p[k]);  // то устанавливаем его и в p[k] позицию результата (туда k бит переходит)
            }
        }
        lo[x] = y;
    }

    // hi: массив предвычисленных значений для старших 16 бит
    vector<uint32_t> hi(1 << 16, 0);
    for (int x = 0; x < (1 << 16); x++) {
        uint32_t t = 0;
        for (int k = 0; k < 16; k++) {
            if ((x >> k) & 1) {
                t |= (1u << p[k + 16]);  // здесь аналогично, но перестановку смотрим на 16 битов дальше, это старшая половина
            }
        }
        hi[x] = t;
    }

    int64_t res = 0; // Сумма может не влезть в 32 бита, используем 64
    
    for (int i = 0; i < n; i++) {
        s = s * a + c;  // LCG шаг (арифметика с uint32_t сама корректно обрежет по модулю 2^32)

        uint32_t t_unsigned = lo[s & 0xFFFF] | hi[s >> 16];  // Извлекаем нужные предвычисленные 32-битные куски

        // Важно! В задаче сказано, что результат является int32 (знаковым).
        // Перекастуем его, чтобы 31-й бит честно отработал как знак минус при прибавлении к 64-битному аккумулятору res.
        int32_t t_signed = static_cast<int32_t>(t_unsigned);
        
        res += t_signed;
    }
    
    cout << res << "\n";

    return 0;
}

