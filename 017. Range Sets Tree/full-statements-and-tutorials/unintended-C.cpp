#include <bits/allocator.h>

/* Простое квадратичное решение, которое всё-таки пролезает в Time Limit и получает Accepted :)
   Максимальные данные за все тесты: 14032ms (время) и 5292kb (память) */

// ==============================================================================
// 1. УКАЗАНИЯ КОМПИЛЯТОРУ (ПРАГМЫ)
// O3: Максимальный уровень алгоритмических оптимизаций компилятора по скорости.
// unroll-loops: Команда разворачивать короткие циклы, чтобы не тратить такты на проверку условий (i < n).
#pragma GCC optimize("O3,unroll-loops")

// target: Разрешает компилятору использовать "тяжелую артиллерию" современных процессоров.
// avx2: SIMD-инструкции (Single Instruction, Multiple Data). Позволяют процессору 
//       умножать/складывать по 8 чисел (256 бит) за один такт.
// bmi/lzcnt: Быстрые инструкции для работы с битами.
#pragma GCC target("avx2,bmi,bmi2,lzcnt,popcnt")
// ==============================================================================

#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// ==============================================================================
// 2. ОПТИМИЗАЦИЯ ВЗЯТИЯ ПО МОДУЛЮ (Barrett Reduction)
// constexpr говорит компилятору, что значение известно до запуска программы.
// Компилятор (видя константу) математически заменяет тяжелую операцию X % MOD 
// на "магическое умножение" и побитовые сдвиги. Это ускоряет операцию в 5-10 раз.
// Суффикс ULL (unsigned long long) гарантирует 64-битный формат, избегая 
// лишних приведений типов (cast overhead) при вычислениях.
// ==============================================================================
static constexpr unsigned long long MOD = 998244353ULL;

unsigned int fast_power(unsigned int a, int n) {
    unsigned int res = 1;
    while (n > 0) {
        if (n % 2 == 1) res = (1ULL * res * a) % MOD;
        a = (1ULL * a * a) % MOD;
        n /= 2;
    }
    return res;
}

int main() {
    // 3. БЫСТРЫЙ ВВОД/ВЫВОД
    // Отвязываем потоки C++ от потоков C, чтобы cin/cout работали без блокировок.
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    if (!(cin >> n >> m)) return 0;

    // ==============================================================================
    // 4. РАННИЙ ВЫХОД (O(1) вместо O(m^2))
    // За каждый шаг мы можем сдвинуться максимум на 2 позиции вправо.
    // Если искомая точка 'n' находится дальше, чем '2 * m', вероятность 
    // туда НЕ попасть за 'm' шагов равна 1. Волне просто не хватит времени дойти.
    // ==============================================================================
    if (n >= 2 * m) {
        cout << 1 << "\n";
        return 0;
    }

    unsigned int p[5];
    unsigned long long sum = 0;
    for(int i = 0; i < 5; i++) {
        cin >> p[i];
        sum += p[i];
    }

    sum %= MOD;
    unsigned int inv_sum = fast_power(sum, MOD - 2);
    
    // 5. КЭШИРОВАНИЕ ДАННЫХ В РЕГИСТРАХ
    // Мы сохраняем вероятности в отдельные переменные.
    // Компилятор положит их в сверхбыстрые регистры процессора, и нам не
    // придется на каждой итерации обращаться к оперативной памяти (массиву p).
    unsigned int p0 = (1ULL * p[0] * inv_sum) % MOD;
    unsigned int p1 = (1ULL * p[1] * inv_sum) % MOD;
    unsigned int p2 = (1ULL * p[2] * inv_sum) % MOD;
    unsigned int p3 = (1ULL * p[3] * inv_sum) % MOD;
    unsigned int p4 = (1ULL * p[4] * inv_sum) % MOD;

    // 6. ПЛОСКИЕ МАССИВЫ (Cache Locality)
    // Вектор векторов (vector<vector>) раскидывает память кусками.
    // Одномерный массив лежит в памяти непрерывно. Процессор легко 
    // угадывает, какие данные понадобятся дальше (Prefetching), и грузит их в L1-кэш.
    int max_needed = max(n + 5, 2 * m + 5);
    vector<unsigned int> r_curr(max_needed, 0);
    vector<unsigned int> r_next(max_needed, 0);


    r_curr[0] = 1; r_curr[1] = 1;
    r_next[0] = 1; r_next[1] = 1;
    
    // 7. ОБЕЩАНИЕ КОМПИЛЯТОРУ (__restrict)
    // Ключевое слово __restrict гарантирует, что изменение данных по указателю next_arr
    // никак не затронет данные по указателю curr. 
    // Без этого компилятор боится применять AVX2 векторизацию (вдруг массивы пересекаются).
    unsigned int* __restrict curr = r_curr.data();
    unsigned int* __restrict next_arr = r_next.data();

    for(int i = 1; i <= m; i++) {
        // ==============================================================================
        // 8. ОПТИМИЗАЦИЯ ОБЛАСТИ ЗАВИСИМОСТИ ("Конус")
        // Вместо того чтобы считать массив с 0 до конца на каждом шаге (m^2 операций),
        // мы считаем только те элементы, которые реально повлияют на ответ в ячейке [n+2].
        // Границы сужаются: на последнем шаге мы считаем только 5 ячеек, на предпоследнем - 9 и т.д.
        // Это отсекает больше половины вычислений впустую!
        // ==============================================================================
        int j_min = max(2, n + 2 - 2 * (m - i));
        int j_max = min(2 + i * 2 - 1, n + 2 + 2 * (m - i));
        
        // 9. ПРИНУДИТЕЛЬНАЯ ВЕКТОРИЗАЦИЯ
        // ivdep (Ignore Vector Dependencies) — заставляет GCC игнорировать мнимые
        // зависимости по данным и жестко векторизовать внутренний цикл.
        // unroll 4 — подсказка развернуть цикл по 4 итерации.
        #pragma GCC ivdep
        #pragma GCC unroll 4
        for(int j = j_min; j <= j_max; j++) {
            // ==============================================================================
            // 10. ОТЛОЖЕННОЕ ВЗЯТИЕ ПО МОДУЛЮ И РУЧНАЯ РАЗВЕРТКА (Loop Unrolling)
            // Мы убрали внутренний цикл for(k=0..4). Вместо него написали вычисления в лоб.
            // Вместо того чтобы делать % MOD 5 раз (после каждого умножения), мы 
            // накапливаем результат в огромной 64-битной переменной (unsigned long long).
            // Переполнения не будет (5 * 10^18 влезает в ULL), а тяжелая операция % MOD 
            // выполняется всего ОДИН раз в самом конце. Это колоссальная экономия тактов процессора.
            // ==============================================================================
            unsigned long long a = 
                1ULL * p0 * curr[j - 2] +
                1ULL * p1 * curr[j - 1] +
                1ULL * p2 * curr[j]     +
                1ULL * p3 * curr[j + 1] +
                1ULL * p4 * curr[j + 2];
            
            next_arr[j] = a % MOD; 
        }
        
        // 11. МГНОВЕННЫЙ СВАП (O(1))
        // Меняем местами только указатели на массивы (число 8 байт), 
        // вместо того чтобы копировать содержимое массивов.
        swap(curr, next_arr);
    }
    
    cout << (MOD + 1 - curr[n + 2]) % MOD << "\n";
    
    return 0;
}
