/*
Идея решения: от графов к скобочным последовательностям

**1. Условие хорошего разбиения:** Разбиение $2n$ точек на $n$ непересекающихся кривых пар сверху и снизу от прямой можно
представить как граф пересечений. Отрезки (пары точек) — это вершины. Если отрезки пересекаются (но не вложены друг в друга),
между ними есть ребро. Известный математический факт: **кривые можно развести сверху и снизу без пересечений тогда и только тогда,
когда этот граф двудольный** (его можно раскрасить в 2 цвета: «верх» и «низ»).

**2. Считаем цветные графы ($P[n]$):**
Давайте для начала считать не просто хорошие разбиения, а *раскрашенные*. То есть мы заранее решаем, какие точки будут соединены
дугами сверху, а какие — снизу. Если мы выбрали $2k$ точек для верхних дуг, чтобы дуги не пересекались, они должны образовывать правильную
скобочную последовательность. Количество таких соединений для $2k$ точек — это **числа Каталана** $C_k$. Для оставшихся $2n - 2k$ точек
снизу — число Каталана для оставшихся точек — то есть $C_{n-k}$ соединений. Итого количество раскрашенных разбиений:
        $$P[n] = \sum_{k=0}^{n} \binom{2n}{2k} C_k C_{n-k}$$ 
(биномиальный коэффициент как множитель, так как это способов выбирать те 2k точек из 2n, которые сверху соединим)

**3. Считаем связные компоненты ($conn[n]$):**
Граф может распадаться на куски. Рассмотрим компоненту связности, которой принадлежит самая первая точка (точка 1). Если в этой компоненте
$k$ пар (то есть $2k$ точек), то эти $2k$ точек разбивают оставшееся пространство на **$2k$ независимых регионов** (между соседними точками
компоненты и после последней). В каждый из этих $2k$ регионов мы можем вставить любые другие независимые раскрашенные разбиения!
Значит, $P[n]$ складывается из перебора размера первой компоненты $k$ и способов раскидать оставшиеся $n-k$ пар по $2k$ регионам.
Если мы посчитаем это с помощью ДП, то сможем «вытащить» оттуда количество связных графов $conn[n]$.

**4. Финальный ответ ($ans[n]$):**
В раскрашенном графе каждая компонента связности может быть раскрашена ровно 2 способами (поменять цвета верхней и нижней доли). Значит,
количество **неокрашенных связных разбиений** — это просто `uncolor_conn[n] = conn[n] / 2`.
Итоговый ответ $ans[n]$ собирается точно так же, как $P[n]$: мы берем первую (неокрашенную) компоненту из $k$ пар, которая делит
пространство на $2k$ регионов, и раскидываем по этим регионам оставшиеся $n-k$ пар, используя уже вычисленные значения $ans$.

Ограничение $N \le 200$ подсказывает, что решение за $O(N^3)$ (перебор $n$, размера первой компоненты и распределения по регионам) залетит моментально.
*/

#include <iostream>
#include <vector>

using namespace std;

long long N, p;

// Быстрое возведение в степень по модулю
long long power(long long base, long long exp) {
    long long res = 1;
    base %= p;
    while (exp > 0) {
        if (exp % 2 == 1) res = (res * base) % p;
        base = (base * base) % p;
        exp /= 2;
    }
    return res;
}

// Обратный элемент по модулю (т.к. p - простое, используем Малую теорему Ферма)
long long modInverse(long long n) {
    return power(n, p - 2);
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    if (!(cin >> N >> p)) return 0;

    // Предподсчет факториалов для сочетаний
    int max_fact = 4 * N + 5;
    vector<long long> fact(max_fact), inv_fact(max_fact);
    fact[0] = 1; inv_fact[0] = 1;
    for (int i = 1; i < max_fact; ++i) {
        fact[i] = (fact[i - 1] * i) % p;
        inv_fact[i] = modInverse(fact[i]);
    }

    auto nCk = [&](int n, int k) -> long long {
        if (k < 0 || k > n) return 0;
        return fact[n] * inv_fact[k] % p * inv_fact[n - k] % p;
    };

    // Предподсчет чисел Каталана: C_k = (2k)! / ((k+1)! * k!)
    vector<long long> C(N + 1);
    for (int k = 0; k <= N; ++k) {
        C[k] = (nCk(2 * k, k) - nCk(2 * k, k - 1) + p) % p;
    }

    // ШАГ 1: Считаем P[n] - кол-во раскрашенных разбиений
    vector<long long> P(N + 1, 0);
    for (int n = 0; n <= N; ++n) {
        for (int k = 0; k <= n; ++k) {
            long long ways = nCk(2 * n, 2 * k);
            ways = (ways * C[k]) % p;
            ways = (ways * C[n - k]) % p;
            P[n] = (P[n] + ways) % p;
        }
    }

    // dpP[r][m] - кол-во способов распределить m пар по r независимым регионам (для раскрашенных)
    // Размер: r до 2*N, m до N
    vector<vector<long long>> dpP(2 * N + 1, vector<long long>(N + 1, 0));
    dpP[0][0] = 1; // 0 пар в 0 регионов
    for (int r = 1; r <= 2 * N; ++r) {
        for (int m = 0; m <= N; ++m) {
            for (int i = 0; i <= m; ++i) {
                dpP[r][m] = (dpP[r][m] + P[i] * dpP[r - 1][m - i]) % p;
            }
        }
    }

    // ШАГ 2: Вычисляем conn[n] - кол-во связных раскрашенных графов
    vector<long long> conn(N + 1, 0);
    vector<long long> uncolor_conn(N + 1, 0);
    long long inv2 = modInverse(2);

    for (int n = 1; n <= N; ++n) {
        long long sum_prev = 0;
        // Отнимаем все случаи, где первая компонента меньше n
        for (int k = 1; k < n; ++k) {
            long long term = (conn[k] * dpP[2 * k][n - k]) % p;
            sum_prev = (sum_prev + term) % p;
        }
        conn[n] = (P[n] - sum_prev + p) % p;
        uncolor_conn[n] = (conn[n] * inv2) % p; // Снимаем раскраску
    }

    // ШАГ 3: Итоговый ответ ans[n] и dpA (аналог dpP, но для неокрашенных)
    vector<long long> ans(N + 1, 0);
    vector<vector<long long>> dpA(2 * N + 1, vector<long long>(N + 1, 0));
    
    ans[0] = 1; // База для пустых регионов
    dpA[0][0] = 1;
    for (int r = 1; r <= 2 * N; ++r) dpA[r][0] = 1;

    for (int n = 1; n <= N; ++n) {
        // Собираем ответ из первой неокрашенной компоненты связности
        for (int k = 1; k <= n; ++k) {
            long long term = (uncolor_conn[k] * dpA[2 * k][n - k]) % p;
            ans[n] = (ans[n] + term) % p;
        }

        // Имея новый ans[n], обновляем слой dpA для m = n
        dpA[0][n] = 0; // В 0 регионов нельзя положить > 0 пар
        for (int r = 1; r <= 2 * N; ++r) {
            for (int i = 0; i <= n; ++i) {
                dpA[r][n] = (dpA[r][n] + ans[i] * dpA[r - 1][n - i]) % p;
            }
        }
    }

    // Вывод результатов от 1 до N
    for (int n = 1; n <= N; ++n) {
        cout << ans[n] << "\n";
    }

    return 0;
}


