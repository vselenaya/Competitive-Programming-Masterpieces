#include <iostream>
#include <vector>

using namespace std;

/*
 * =================================================================================================
 * ИДЕЯ РЕШЕНИЯ (Дерево Отрезков + Фибоначчи)
 * =================================================================================================
 * 1. Состояния в узлах ДО:
 * Каждый узел, отвечающий за отрезок [l, r], хранит две суммы:
 * t1 = sum(a[l + i - 1] * F[i])     для i от 1 до len
 * t2 = sum(a[l + i - 1] * F[i + 1]) для i от 1 до len
 * * 2. Магия объединения узлов (pull):
 * Когда мы сливаем левого и правого ребенка, правый ребенок "сдвинут" на длину левого (пусть L).
 * Нам нужно пересчитать коэффициенты для правого. Спасает классическое тождество Фибоначчи:
 * F[L + i] = F[L - 1] * F[i] + F[L] * F[i + 1]. 
 * Именно поэтому мы храним и t1 (где F[i]), и t2 (где F[i+1]). Мы можем линейно выразить
 * любой сдвиг правого отрезка через его t1 и t2!
 *
 * 3. Массовое обновление (lazy propagation):
 * Прибавляем x на отрезке [l, r] длины len.
 * t1 увеличится на x * sum(F[1]...F[len]).
 * t2 увеличится на x * sum(F[2]...F[len + 1]).
 * Эти суммы можно брать за О(1) из заранее предподсчитанного массива префиксных сумм последовательности Фибоначчи.
 *
 * 4. Запрос суммы (query):
 * Если узел полностью лежит в запросе [cl, cr], его нужно "сдвинуть" на k = l - cl.
 * По тому же тождеству: искомая сумма = F[k] * t2 + F[k - 1] * t1.
 * (Отдельно обрабатываем k=0 и k=1, чтобы не вылезти за границы массива Фибоначчи).
 * =================================================================================================
 */

// Константы
static constexpr int MOD = 1'000'000'007;
static constexpr int MAXN = 200005;

// Глобальные массивы для предподсчета (конечно, по модулю)
long long F[MAXN + 5];      // F[i] = i-е число Фибоначчи
long long prefF[MAXN + 5];  // prefF[i] = сумма F[1] + ... + F[i]

// Узел дерева отрезков
struct Node {
    long long t1 = 0;   // Сумма a_i * F_i
    long long t2 = 0;   // Сумма a_i * F_{i+1}
    long long lazy = 0; // Отложенный платеж (сколько нужно прибавить к каждому элементу потомка)
};

Node tree[4 * MAXN];  // вершины ДО храним в массиве (у вершины, чей индекс равен i, дети имеют индексы 2i и 2i+1)

// Предподсчет чисел Фибоначчи и их префиксных сумм
void precalc() {
    F[0] = 0;
    F[1] = 1;
    prefF[0] = 0;
    prefF[1] = 1;
    for (int i = 2; i < MAXN + 5; i++) {
        F[i] = (F[i - 1] + F[i - 2]) % MOD;
        prefF[i] = (prefF[i - 1] + F[i]) % MOD;
    }
}

// Функция слияния детей (пересчет значений t1 и t2 для текущего узла: его индекс (в массиве tree) равен v, отвечает он за отрезок [l, r])
void pull(int v, int l, int r) {
    int mid = l + (r - l) / 2;
    int len_left = mid - l + 1; // L - длина левого ребенка

    // Берем нужные коэффициенты для сдвига правого ребенка
    long long F_L_minus_1 = F[len_left - 1];
    long long F_L = F[len_left];
    long long F_L_plus_1 = F[len_left + 1];

    int left = 2 * v;  // потомки (дети) текущей вершины v (их индексы в tree)
    int right = 2 * v + 1;

    // Сдвигаем t1 правого ребенка на L
    long long right_t1_shifted = (F_L_minus_1 * tree[right].t1 % MOD + F_L * tree[right].t2 % MOD) % MOD;
    tree[v].t1 = (tree[left].t1 + right_t1_shifted) % MOD;

    // Сдвигаем t2 правого ребенка на L
    long long right_t2_shifted = (F_L * tree[right].t1 % MOD + F_L_plus_1 * tree[right].t2 % MOD) % MOD;
    tree[v].t2 = (tree[left].t2 + right_t2_shifted) % MOD;
}

// Применить отложенное прибавление значения x к узлу v
void apply(int v, int l, int r, long long x) {
    int len = r - l + 1; // длина отрезка [l, r], за который отвечает узел v
    
    // Сумма F_1 ... F_len = prefF[len]
    long long add_t1 = (x % MOD) * prefF[len] % MOD;
    tree[v].t1 = (tree[v].t1 + add_t1) % MOD;
    
    // Сумма F_2 ... F_{len+1} = prefF[len + 1] - F[1]
    long long sum_t2 = (prefF[len + 1] - F[1] + MOD) % MOD; // +MOD чтобы не было отрицательных
    long long add_t2 = (x % MOD) * sum_t2 % MOD;
    tree[v].t2 = (tree[v].t2 + add_t2) % MOD;
    
    // Запоминаем, что детям тоже надо будет прибавить x
    tree[v].lazy = (tree[v].lazy + x) % MOD;
}

// Проталкивание отложенных операций детям
void push(int v, int l, int r) {
    if (tree[v].lazy != 0) {
        int mid = l + (r - l) / 2;
        apply(2 * v, l, mid, tree[v].lazy);           // левому ребенку
        apply(2 * v + 1, mid + 1, r, tree[v].lazy);   // правому ребенку
        tree[v].lazy = 0;                                   // очищаем обещание
    }
}

// Построение дерева
void build(int v, int l, int r, const vector<long long>& a) {
    if (l == r) { // Лист
        tree[v].t1 = a[l] * F[1] % MOD; // a[l] * 1
        tree[v].t2 = a[l] * F[2] % MOD; // a[l] * 1
        return;
    }
    int mid = l + (r - l) / 2;
    build(2 * v, l, mid, a);
    build(2 * v + 1, mid + 1, r, a);
    pull(v, l, r); // сливаем
}

// Обновление на отрезке: делаем +x на отрезке [ql, qr]
// само обновление начинаем с узла ДО v, который отвечает за отрезок [l, r] 
void update(int v, int l, int r, int ql, int qr, long long x) {
    if (ql > r || qr < l) return; // Отрезки не пересекаются
    if (ql <= l && r <= qr) {     // Текущий отрезок полностью внутри запроса
        apply(v, l, r, x);
        return;
    }
    push(v, l, r);                // Проталкиваем перед погружением
    int mid = l + (r - l) / 2;
    update(2 * v, l, mid, ql, qr, x);
    update(2 * v + 1, mid + 1, r, ql, qr, x);
    pull(v, l, r);                // Пересчитываем текущий узел после обновления детей
}

// Запрос суммы (ответа) на отрезке [ql, qr]. Здесь мы пытаемся вернуть значение F[1] * a[ql] + F[2] * a[ql+1] + ... + F[qr-ql+1] * a[qr].
// Но как обычно в ДО запрос делается для конкртеного поддерева с вершиной v, отвечающей за отрезок [l, r] (отрезок массива a).
// (то есть, как и обычно в ДО, мы в ответе не учитываем элементы a[ql...qr], не попавшие в запрашиваемый интервал [l...r] ->
//  -> они заменяются на нейтральные элементы (относительно запрашиваемой операции, в данном случае 0) и эти куски дозапрашиваются у соседей)
long long query(int v, int l, int r, int ql, int qr) {
    if (ql > r || qr < l) return 0; // Вне запроса даем 0
    if (ql <= l && r <= qr) {       // Узел полностью лежит в запросе [ql, qr]
        int k = l - ql;             // Насколько узел сдвинут относительно начала запроса
                                    // (мы отвечаем только суммой F[k+1] * a[l] + ... + F[k+r-l+1] * a[r], предыдущие k и последующие
                                    //  слагаемые должны были быть запрошены у других потомков выше в рекурсии...)
        
        if (k == 0) return tree[v].t1; // Если совпадает, просто возвращаем t1
        if (k == 1) return tree[v].t2; // Если сдвинут на 1, возвращаем t2
        
        // Иначе комбинируем по правилу из разбора в начале: F_k * t2 + F_{k-1} * t1
        long long res = (F[k] * tree[v].t2 % MOD + F[k - 1] * tree[v].t1 % MOD) % MOD;
        return res;
    }
    push(v, l, r);                  // Проталкиваем перед погружением
    int mid = l + (r - l) / 2;
    
    // Сумма ответов слева и справа (относительный сдвиг ql уже заложен в логике базы)
    long long res_left = query(2 * v, l, mid, ql, qr);
    long long res_right = query(2 * v + 1, mid + 1, r, ql, qr);
    
    return (res_left + res_right) % MOD;
}

int main() {
    // Ускоряем ввод-вывод
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    precalc(); // Обязательно предподсчитываем Фибоначчи!

    int n, q;
    cin >> n >> q;

    // Массив будем индексировать с 1 для удобства
    vector<long long> a(n + 1);
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }

    build(1, 1, n, a);

    for (int i = 0; i < q; i++) {
        char type;
        cin >> type;
        if (type == '+') {
            int l, r;
            long long x;
            cin >> l >> r >> x;
            update(1, 1, n, l, r, x);
        } else if (type == '?') {
            int l, r;
            cin >> l >> r;
            long long ans = query(1, 1, n, l, r);
            cout << ans << "\n";
        }
    }

    return 0;
}
