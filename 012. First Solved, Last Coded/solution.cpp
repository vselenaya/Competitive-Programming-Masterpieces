#include <iostream>
#include <vector>

using namespace std;

/*
 * =================================================================================================
 * ИДЕЯ РЕШЕНИЯ:
 * =================================================================================================
 *
 * Решать будем через ДП (динамическое программирование), состояние которого:
 * memo[i][j][len] — можно ли подотрезок A[i ... i+len-1] превратить в B[j ... j+len-1], 
 * начав и закончив с пустым стеком (то есть решить все задачи в порядке A[i ... i+len-1] и их же закодить
 * в порядке B[j ... j+len-1]) ? Если да, то в качестве значения храним индекс k (точку разделения),
 * чтобы потом восстановить ответ. Этот индекс k означает, что A[i] переходит в B[j+k], то есть
 * первый элемент первого отрезка переходит на k-ую позицию второго (первая решенная заадча будет закодена k-ой).
 *
 * Переход:
 * Самый первый элемент A[i] обязательно попадёт на дно пустого стека (начинаем в ДП с пустого стека).
 * Когда-нибудь мы его достанем, и он станет элементом B[j+k] (где 0 <= k < len).
 * Для этого нужно, чтобы:
 * 1. A[i] == B[j+k] (элементы физически совпали).
 * 2. Всё, что мы будем класть и доставать поверх A[i], должно сформировать
 * вывод до B[j+k]. То есть подотрезок A[i+1 ... i+k] должен превратиться в B[j ... j+k-1].
 * 3. После того как мы достали A[i], стек СНОВА ПУСТ (так делаем наше ДП)! Значит, оставшийся "хвост"
 * A[i+k+1 ... i+len-1] должен превратиться в B[j+k+1 ... j+len-1].
 *
 * P.S. На контесте задачу решить не удалось, хотя было понятно, что она на ДП. Но я никак не мог придумать, что же делать
 * со стеком, как его запихнуть в состояние динамики... но фишка оказалась в том, что нам и не нужно как-то упихивать стек в динамику,
 * нужно просто проводить динамику между точками, когда стек пуст! И это классная идея для аналогичных задач! Глобальный вывод по ДП:
 * Часто, когда в задаче есть какая-то структура данных с состоянием (стек, очередь, рюкзак сложной формы), кажется, что
 * нужно тащить всё это состояние в параметры ДП: dp[i][j][stack_contents]. Но это моментально приводит к взрыву состояний.
 * Секрет в том, чтобы искать «узкие горлышки» (bottlenecks) или точки сброса (reset points). В задачах со стеком или
 * скобочными последовательностями такие точки — это моменты, когда структура полностью пуста. Мы абстрагируемся от того,
 * что происходит внутри, и разбиваем задачу на независимые блоки, которые гарантированно начинаются и заканчиваются с пустым стеком.
 * =================================================================================================
 */

// Константы
static constexpr int MAXN = 105;
static constexpr int FALSE_STATE = -2;
static constexpr int UNVISITED = -1;

// memo[i][j][len] хранит выбранное значение k (сдвиг), если состояние достижимо.
// Если недостижимо, хранит FALSE_STATE. Если не посещали - UNVISITED.
int memo[MAXN][MAXN][MAXN];

// Основная функция ленивой динамики
int solve(int i, int j, int len, const vector<int>& A, const vector<int>& B) {
    if (len == 0) return 0; // Пустые отрезки всегда можно превратить друг в друга
    
    // Мемоизация: если уже считали это состояние, возвращаем результат
    if (memo[i][j][len] != UNVISITED) {
        return memo[i][j][len];
    }

    // Перебираем k — количество элементов, которые будут выведены ПЕРЕД A[i]
    for (int k = 0; k < len; ++k) {
        // Условие 1: Первый положенный элемент A[i] должен совпасть с (k+1)-ым выведенным элементом B[j+k]
        if (A[i] == B[j + k]) {
            // Условие 2: Внутренний блок (над A[i] в стеке) сходится
            int left_part = solve(i + 1, j, k, A, B);
            
            // Условие 3: Правый блок (после извлечения A[i] из стека) сходится
            int right_part = solve(i + k + 1, j + k + 1, len - k - 1, A, B);
            
            // Если обе подзадачи решаемы, мы нашли валидное разбиение!
            if (left_part != FALSE_STATE && right_part != FALSE_STATE) {
                return memo[i][j][len] = k; // Запоминаем точку разбиения
            }
        }
    }
    
    // Если ни один k не подошел
    return memo[i][j][len] = FALSE_STATE;
}

// Восстановление ответа (строим строку 'S' и 'C')
void restore(int i, int j, int len) {
    if (len == 0) return;
    
    int k = memo[i][j][len]; // Достаем успешное разбиение из кэша
    
    cout << "S";                                  // Кладём A[i] в стек
    restore(i + 1, j, k);                         // Обрабатываем всё, что должно выйти до A[i]
    cout << "C";                                  // Достаём A[i] (он становится B[j+k])
    restore(i + k + 1, j + k + 1, len - k - 1);   // Обрабатываем хвост после опустошения стека
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;

    vector<int> A(n);
    for (int i = 0; i < n; ++i) {
        cin >> A[i];
    }

    vector<int> B(n);
    for (int i = 0; i < n; ++i) {
        cin >> B[i];
    }

    // Инициализируем 3D массив кэша значением UNVISITED (-1)
    for (int i = 0; i <= n; ++i) {
        for (int j = 0; j <= n; ++j) {
            for (int len = 0; len <= n; ++len) {
                memo[i][j][len] = UNVISITED;
            }
        }
    }

    // Запускаем ДП для всего отрезка длины n
    if (solve(0, 0, n, A, B) != FALSE_STATE) {
        cout << "YES\n";
        restore(0, 0, n); // Если ответ YES, рекурсивно раскручиваем путь
        cout << "\n";
    } else {
        cout << "NO\n";
    }

    return 0;
}
