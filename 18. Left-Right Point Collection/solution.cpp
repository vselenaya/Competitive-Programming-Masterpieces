#pragma GCC optimize("O3,unroll-loops")
#include <iostream>
#include <vector>
#include <algorithm>
#include <array>

/*
Разбор идеи:

Ключевое наблюдение заключается в том, что время, затраченное на поход по магазинам в конкретный день,
зависит только от самых дальних магазинов, которые мы посетили слева (отрицательные координаты) и справа
(положительные координаты) от отеля.

Если мы отсортируем все координаты магазинов по возрастанию, то оптимальная стратегия всегда будет заключаться в том,
чтобы «откусывать» магазины с краев. То есть в любой момент времени множество еще не посещенных магазинов представляет
собой непрерывный отрезок [l, r].


Как строится день:

- Каждый день мы можем выбирать магазины либо с левого края оставшегося отрезка (самые большие по модулю отрицательные),
  либо с правого (самые большие положительные).

- Если мы впервые за день берем магазин слева x_l <= 0, мы платим за дорогу туда-обратно: 2 * |x_l| + k. Все последующие магазины слева,
  которые мы возьмем в этот же день, обойдутся нам только во время примерки k, так как они находятся по пути.

- То же самое справа: первый магазин x_r >= 0 обходится в 2 * x_r + k, остальные справа в этот же день — просто k.


Динамическое программирование:

- Мы будем сжимать отрезок непосещенных магазинов от [1, n] (это индексы непосещенных магазинов,
  отсортированных слева направо, в 1-индексации) до пустого.

- Вместо того чтобы делать текущее затраченное время частью состояния ДП (что невозможно, так как T <= 10^18), мы сделаем его значением ДП.

Состояние: dp[len][l][state]
- len — сколько магазинов осталось посетить.
- l — индекс левого непосещенного магазина (тогда правый r = l + len - 1).
- state — битовая маска от 0 до 3. Показывает, заплатили ли (то есть ходили ли примерять) мы уже в текущий день за
  поход налево (0-ой бит) и за поход направо (1-ый бит).
- Значение = Пара {минимальное число дней, время затраченное в текущий день}. Мы минимизируем эту пару лексикографически.


Оптимизация памяти:

Так как для вычисления отрезков длины len - 1 нам нужны только данные от отрезков длины len, мы можем хранить только два слоя ДП:
dp_curr и dp_next. Это снижает потребление памяти с гигантского трехмерного массива до O(N), что легко укладывается в любые лимиты.


P.S. 1. Почему в «Ботинках» непосещенные магазины всегда образуют отрезок?
Все дело в том, как мы перемещаемся. Представим, что мы посетили магазины L (слева) и R (справа) в один день.
Чтобы это сделать, мы прошли путь от отеля (0) до L, затем до R (или наоборот) и вернулись в отель. Если между L и R есть
какой-то магазин U, который мы не посетили, это значит, что мы буквально прошли мимо его дверей, но не зашли. Если бы мы зашли
в него в этот же день, мы бы потратили на него ровно k времени (только примерка), потому что время на дорогу до его координаты
уже оплачено походом к более дальним магазинам L или R. Если же мы оставим U на другой день, нам придется снова оплачивать дорогу
к нему от отеля. Это всегда менее выгодно или равносильно. Поэтому оптимальная стратегия — всегда "съедать" магазины с краев.
Если мы дошли до какой-то дальней точки, нужно обязательно забрать все магазины по пути. Значит, то, что осталось непосещенным,
всегда кучкуется в неразрывный отрезок по центру.
*/


using namespace std;

// Структура для хранения значения ДП. 
// Сортировка по умолчанию лексикографическая: сначала минимизируем дни, затем время.
struct State {
    int d;          // Количество дней
    long long t;    // Время, потраченное в текущий (последний) день
    
    bool operator<(const State& o) const {
        if (d != o.d) return d < o.d;
        return t < o.t;
    }
};

// Константа, обозначающая недостижимое состояние
const State INF_STATE = {1000000000, 2000000000000000000LL};

int main() {
    // Ускоряем ввод/вывод
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    long long k, T;
    cin >> n >> k >> T;

    // Считываем координаты в 1-индексации
    vector<long long> x(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> x[i];
    }

    // Сортируем магазины. Теперь они упорядочены слева направо.
    sort(x.begin() + 1, x.end());

    // dp_curr хранит ответы для текущей длины оставшегося отрезка.
    // Размер n + 2, чтобы безопасно обращаться к l + 1.
    // [l][state] -> {days, time}  (state, напомним, это битовая маска!)
    vector<array<State, 4>> dp_curr(n + 2, {INF_STATE, INF_STATE, INF_STATE, INF_STATE});

    // Базовый случай: все n магазинов еще не посещены, мы стоим на l = 1.
    // Начинаем 1-й день, потрачено 0 времени.
    dp_curr[1][0] = {1, 0};

    // Постепенно "съедаем" магазины с краев, уменьшая длину оставшегося отрезка
    for (int len = n; len >= 1; --len) {
        // dp_next будет хранить ответы для отрезков длины len - 1
        vector<array<State, 4>> dp_next(n + 2, {INF_STATE, INF_STATE, INF_STATE, INF_STATE});

        for (int l = 1; l <= n - len + 1; ++l) {
            int r = l + len - 1;

            // ШАГ 1: Завершение текущего дня.
            // Если в текущем дне уже есть магазины (state > 0), мы всегда можем 
            // решить закончить этот день и лечь спать. Начинаем новый день (state = 0).
            for (int state = 1; state < 4; ++state) {
                if (dp_curr[l][state].d != INF_STATE.d) {
                    State next_s = {dp_curr[l][state].d + 1, 0};
                    if (next_s < dp_curr[l][0]) {
                        dp_curr[l][0] = next_s;
                    }
                }
            }

            // ШАГ 2: Переходы (берем магазин с левого или правого края)
            for (int state = 0; state < 4; ++state) {
                if (dp_curr[l][state].d == INF_STATE.d) continue;

                int d = dp_curr[l][state].d;
                long long t = dp_curr[l][state].t;

                // ВАРИАНТ А: Берем крайний левый магазин (только если он <= 0)
                // Если он > 0, то все оставшиеся > 0, и выгоднее брать их справа (как r).
                if (x[l] <= 0) {
                    long long cost = k;
                    // Если мы еще не ходили налево в этот день, нужно оплатить дорогу
                    if ((state & 1) == 0) {
                        cost += 2LL * (-x[l]); 
                    }
                    
                    // Проверяем, успеем ли мы до конца дня
                    if (t + cost <= T) {
                        State ns = {d, t + cost};
                        // Переходим в состояние len - 1 (сдвигаем левую границу)
                        // Включаем 0-й бит (посетили левую сторону)
                        if (ns < dp_next[l + 1][state | 1]) {
                            dp_next[l + 1][state | 1] = ns;
                        }
                    }
                }

                // ВАРИАНТ Б: Берем крайний правый магазин (только если он >= 0)
                if (x[r] >= 0) {
                    long long cost = k;
                    // Если мы еще не ходили направо в этот день, оплачиваем дорогу
                    if ((state & 2) == 0) {
                        cost += 2LL * x[r];
                    }
                    
                    if (t + cost <= T) {
                        State ns = {d, t + cost};
                        // Переходим в состояние len - 1 (сдвигаем правую границу)
                        // Включаем 1-й бит (посетили правую сторону)
                        if (ns < dp_next[l][state | 2]) {
                            dp_next[l][state | 2] = ns;
                        }
                    }
                }
            }
        }
        // Переходим к отрезкам меньшей длины
        dp_curr = dp_next;
    }

    // Когда len = 0, все магазины разобраны. 
    // Оставшийся пустой отрезок "схлопнулся" в какой-то точке l.
    int ans = INF_STATE.d;
    for (int l = 1; l <= n + 1; ++l) {
        for (int state = 0; state < 4; ++state) {
            if (dp_curr[l][state].d < ans) {
                ans = dp_curr[l][state].d;  // просто берём минимум
            }
        }
    }

    cout << ans << "\n";
    return 0;
}
