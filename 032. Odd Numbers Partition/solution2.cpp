#include <iostream>
#include <vector>
#include <numeric>

using namespace std;

/* Решение авторов (видимо японцев, потому что разбор был на японском...) предполагало симпатичный
конструктив (что в целом логично, так как для перебора с возвратом, который пусть и проще придумывается, непонятно
как доказывать, что он отработает быстро (не выродится в экспоненциальный перебор или вообще не заблокирует
элементы для других кучек, собирая предыдущую...) -- в общем, предыдущее решение чисто спортивное,
текущее же более формальное). Ниже он реализован с кратким описанием идеи. */
/*
 ╔════════════════════════════════════════════════════════════════════════════════════════════════════╗
 ║                          РАЗБОР ЗАДАЧИ (ИДЕЯ ИЗ РАЗБОРА)                                           ║
 ╚════════════════════════════════════════════════════════════════════════════════════════════════════╝
   
   Дано N палочек нечетной длины: 1, 3, 5, ..., 2N-1. 
   Сумма всех длин равна N². Нам нужно разделить их на M кучек с равной суммой.
   Длина каждой кучки должна быть равна N²/M. Из разбора следует, что мы ищем разбиение 
   на M = p кучек, где p — наименьший нечетный простой делитель числа N (если N нечетное).

   [СЛУЧАЙ 1] N — простое число (или N = 1)
   • При нечетном простом N невозможно сделать разбиение (M не может быть 1, N или N², например, из ограничений,
     которые в предыдущем решении проверяются функцией can_partiate()).
   • Выводим: -1

   [СЛУЧАЙ 2] N — четное число (M = N / 2)
   • Склеиваем пары "самая короткая + самая длинная" с краев: (1, 2N-1), (3, 2N-3), ...
   • Каждая пара дает сумму 2N. Всего кучек M = N/2. В каждой кучке по 2 палочки.

   [СЛУЧАЙ 3] N — составное нечетное число (N = p * q, где 3 ≤ p ≤ q)
   • Находим наименьший простой делитель p. Количество кучек M = p.
   • ШАГ A (Латинский квадрат p × p):
     Первые p² палочек (от 1 до 2*p² - 1) раскладываем в матрицу p × p.
     Используя сдвиг строк (латинский квадрат), распределяем их по p кучкам так, 
     чтобы в каждой кучке было ровно по одному элементу из каждой строки и столбца.
   • ШАГ B (Жадное спаривание остатка):
     Оставшиеся палочки (от 2*p² + 1 до 2N - 1) разбиваем на пары с краев: "маленькая + большая".
     Каждая пара дает одинаковую сумму. Эти пары циклически дописываем в наши p кучек.


    Пример для случая 3. Пусть N=15 -> наименьший простой делитель p=3. Раскладываем первые p*p = 9 чисел
    по квадратной таблице p на p. Для этого заполняем её подряд, строка за строкой. Но каждый раз сдвигаем начало строки, то есть
    с какого элемента начинать заполнять очередную строку (но строка заполняется циклично: после последнего элемента строки, если она ещё не заполнена,
    так как начали не с её начала, то просто переходим на начало и пишем оттуда):
    1  3  5     // в первой строки начали с первой ячейки
    11 7  9     // во второй строке произошёл сдвиг -> начали уже со второй ячейки (но сами строки циклично заполняем, поэтому элемент 11, который идёт после 9, оказался на первом месте)
    15 17 13    // в третьей строки начали с 3 позиции (там написали 13), а потом циклично перешли на первую (так как строка кончилась) и дописали оттуда оставшееся
    
    Нетрудно заметить, что в каждом столбце сумма одинакова! В этом и смысл! Это наши p кучек, куда докидываем осташиеся пары с равной суммой
    (это возможно, тк осталось N-p палочек, что делится на 2p (по 2 палочки в каждую из p кучек)).  
*/

// Функция для поиска наименьшего простого делителя числа
int get_min_prime_divisor(int n) {
    if (n % 2 == 0) return 2;
    for (int i = 3; i * i <= n; i += 2) {
        if (n % i == 0) return i;
    }
    return n;
}

int main() {
    // Оптимизация ввода-вывода
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;

    // Случай 1: N <= 1 или N — простое нечетное число
    int p = get_min_prime_divisor(n);
    if (n <= 3 || (n % 2 != 0 && p == n)) {
        cout << -1 << "\n";
        return 0;
    }

    // Случай 2: N — четное число
    if (n % 2 == 0) {
        int m = n / 2;
        cout << m << "\n";
        for (int i = 0; i < m; ++i) {
            int left_stick = 2 * i + 1;
            int right_stick = 2 * n - 1 - 2 * i;
            cout << 2 << " " << left_stick << " " << right_stick << "\n";
        }
        return 0;
    }

    // Случай 3: N — составное нечетное число (N = p * q, где p минимальное прострое в разложении N, а q что осталось)
    // Количество результирующих кучек будет равно p
    int m = p; 
    vector<vector<int>> groups(m);

    // ШАГ A: Заполнение матрицы p x p по принципу латинского квадрата
    int current_stick = 1;
    for (int row = 0; row < p; ++row) {
        for (int col = 0; col < p; ++col) {
            // Сдвиг определяет, в какую из m групп попадет текущая палочка (несложно проврить, что в итоге, в каждой группе будет одинаковая сумма!)
            int group_idx = (row + col) % m;
            groups[group_idx].push_back(current_stick);
            current_stick += 2;
        }
    }

    // ШАГ B: Спаривание оставшихся палочек с краев и распределение по группам
    int remaining_pairs = (n - p * p) / 2; // общее число пар в остатке (первые p^2 уже распределлил по группам, в них сейчас одинаковая сумма)
    int left_idx = 0;                      // индекс для циклического обхода групп
    
    for (int i = 0; i < remaining_pairs; ++i) {
        int pair_left = current_stick;
        int pair_right = 2 * n - 1 - (current_stick - (2 * p * p + 1));
        
        groups[left_idx].push_back(pair_left);
        groups[left_idx].push_back(pair_right);
        
        current_stick += 2;
        left_idx = (left_idx + 1) % m; // переходим к следующей кучке
    }

    // Вывод результата
    cout << m << "\n";
    for (int i = 0; i < m; ++i) {
        cout << groups[i].size();
        for (int stick : groups[i]) {
            cout << " " << stick;
        }
        cout << "\n";
    }

    return 0;
}
