/*
 * =======================================================================================
 * РЕШЕНИЕ ЗАДАЧИ ПРО KETEK (ПАЛИНДРОМНЫЕ ПРЕДЛОЖЕНИЯ)
 * =======================================================================================
 * * 1. СУТЬ ЗАДАЧИ
 * Ketek — это предложение, которое читается одинаково по словам слева направо и 
 * справа налево (например: "a b a", "fall leaves after leaves fall").
 * Нам дана строка (до 30 000 символов), где есть буквы и знаки '?'. Мы можем заменять 
 * '?' на любые буквы (a-z) и вставлять пробелы. Нужно найти количество уникальных Ketek.
 * * По сути, пробелы разбивают строку на слова: W_1, W_2, ..., W_k.
 * Чтобы это был Ketek, первое слово должно полностью совпадать с последним, 
 * второе с предпоследним и так далее. Это означает, что мы "отщипываем" одинаковые 
 * префиксы и суффиксы от нашей строки, шаг за шагом двигаясь к центру!
 * * 2. ПОДХОД К РЕШЕНИЮ (ДИНАМИКА)
 * Пусть DP[i] — количество способов разбить оставшуюся часть строки, если мы 
 * уже "отщипнули" ровно i символов слева (и, соответственно, i символов справа).
 * Оставшаяся подстрока — это S[i ... N - 1 - i].
 * * Чтобы посчитать DP[i], мы перебираем длину следующего слова. Пусть оно заканчивается 
 * на индексе k-1. Тогда новое слово откусит кусок S[i ... k-1] слева и такой же кусок 
 * симметрично справа.
 * Формула перехода: DP[i] = W_0(i) + SUM ( DP[k] * Val(i, k) ), где:
 * - W_0(i) — количество способов превратить центральную часть S[i ... N - 1 - i]
 *   в ОДНО целое слово (это 26 в степени количества '?').
 * - Val(i, k) — количество способов сделать левое и правое слово одинаковыми.
 * * 3. БОРЬБА С ЛИМИТОМ ПАМЯТИ ("unusual Memory Limit")
 * Обычная динамика потребовала бы двумерного массива или предрасчета совпадений 
 * подстрок, что заняло бы O(N^2) памяти (для N=30000 это гигабайты!). Но мы умнее!
 * * Заметим интересное свойство: когда мы сравниваем подстроку слева и подстроку справа, 
 * индексы сравниваемых символов всегда лежат на одной "диагонали", если смотреть 
 * на их сумму! Разница индексов D = N - (i + k). 
 * * Это значит, что если мы будем вычислять DP[i] с конца (от центра строки N/2 к 0), 
 * мы можем поддерживать значения Val для всех активных диагоналей в ОДНОМЕРНОМ 
 * массиве diag_val[u], где u = i + k.
 * Переход для диагонали делается за O(1): мы просто домножаем старое значение на 
 * два новых символа по краям!
 * * Итог: время O(N^2), а память всего O(N) — идеально ложится в любые лимиты!
 * P.S. Решение получает Accepted и тратит максимум по тестам: 1.515s (время) и 3.11Mb (память).
 * =======================================================================================
 */

#pragma GCC optimize("O3,unroll-loops")
#include <iostream>
#include <vector>
#include <string>

using namespace std;

const long long MOD = 998244353;

// Функция для подсчета количества способов совпадения двух символов
inline long long get_match_ways(const string& s, int D, int x) {
    char c1 = s[x];
    char c2 = s[x + D];
    if (c1 == '?' && c2 == '?') return 26; // Оба неизвестны - 26 вариантов
    if (c1 == '?' || c2 == '?') return 1;  // Один неизвестен - подстраиваем его под букву
    if (c1 == c2) return 1;                // Обе буквы совпадают - 1 вариант
    return 0;                              // Буквы разные - совпадение невозможно
}

int main() {
    // Ускоряем ввод-вывод
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    string s;
    if (!(cin >> s)) return 0;

    int n = s.length();
    int half_n = n / 2;

    // Предвычисляем степени 26 по модулю для быстрого доступа
    vector<long long> power26(n + 1, 1);
    for (int i = 1; i <= n; ++i) {
        power26[i] = (power26[i - 1] * 26) % MOD;
    }

    // Предвычисляем префиксные суммы количеств '?' в строке
    vector<int> pref_q(n + 1, 0);
    for (int i = 0; i < n; ++i) {
        pref_q[i + 1] = pref_q[i] + (s[i] == '?' ? 1 : 0);
    }

    // Функция, которая за О(1) выдает количество '?' на отрезке [L, R]
    auto get_q_count = [&](int L, int R) {
        if (L > R) return 0;
        return pref_q[R + 1] - pref_q[L];
    };

    // dp[i] — ответ для подстроки после отсечения i символов слева и справа
    vector<long long> dp(half_n + 1, 0);
    
    // diag_val[u] хранит произведение совпадений символов для текущей диагонали u = i + k
    vector<long long> diag_val(n + 1, 0);

    // Базовый случай: заполняем DP вариантами, где центральная часть становится 1 словом
    for (int i = 0; i <= half_n; ++i) {
        int q_count = get_q_count(i, n - 1 - i);
        dp[i] = power26[q_count];
    }

    // Вычисляем DP, двигаясь от центра строки к ее краям
    for (int i = half_n; i >= 0; --i) {
        
        // 1. Расширяем уже существующие активные диагонали
        for (int u = 2 * i + 3; u <= half_n + i; ++u) {
            if (diag_val[u] == 0) continue; // Небольшая оптимизация скорости
            
            // Получаем варианты совпадений для новых символов по краям отрезка
            long long p1 = get_match_ways(s, n - u, i);
            long long p2 = get_match_ways(s, n - u, u - i - 1);
            
            if (p1 == 0 || p2 == 0) {
                diag_val[u] = 0; // Цепочка совпадений оборвалась :(
            } else {
                diag_val[u] = (diag_val[u] * (p1 * p2)) % MOD;
            }
        }

        // 2. Инициализируем новые диагонали, которые "рождаются" на этом шаге i
        if (i + 1 <= half_n) {
            int u = 2 * i + 1;
            diag_val[u] = get_match_ways(s, n - u, i);
        }
        if (i + 2 <= half_n) {
            int u = 2 * i + 2;
            long long p1 = get_match_ways(s, n - u, i);
            long long p2 = get_match_ways(s, n - u, i + 1);
            diag_val[u] = (p1 * p2) % MOD;
        }

        // 3. Собираем значения для текущего состояния DP
        for (int k = i + 1; k <= half_n; ++k) {
            int u = i + k;
            if (diag_val[u] > 0) {
                dp[i] = (dp[i] + dp[k] * diag_val[u]) % MOD;
            }
        }
    }

    // Ответ для всей строки (0 отсеченных символов)
    cout << dp[0] << "\n";

    return 0;
}
