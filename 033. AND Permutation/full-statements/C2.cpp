/*
 * =======================================================================================
 * РЕШЕНИЕ ЗАДАЧИ ПРО РОБОТА-УБОРЩИКА
 * =======================================================================================
 * * 1. МОНОТОННОСТЬ И БИНАРНЫЙ ПОИСК
 * Если робот размера S может убрать всю доступную площадь (то есть все пустые клетки 
 * достижимы и образуют единую зону покрытия), то и любой робот меньшего размера (S-1) 
 * тоже справится с этой задачей. Это классическое свойство монотонности! 
 * Благодаря ему мы можем искать максимальный размер S с помощью бинарного поиска 
 * в диапазоне от 1 до min(N, M).
 * * 2. ПОИСК ВАЛИДНЫХ ПОЗИЦИЙ ДЛЯ РОБОТА (2D Префиксные суммы)
 * Робот размера S x S может быть помещен в клетку (r, c) [это левый верхний угол], 
 * только если внутри квадрата от (r, c) до (r+S-1, c+S-1) нет препятствий. 
 * Чтобы проверять это за О(1), мы один раз до начала поиска строим массив двумерных 
 * префиксных сумм для сетки препятствий. 
 * * 3. ПРОВЕРКА ДОСТИЖИМОСТИ (BFS)
 * Выбрав размер S, мы находим ПЕРВУЮ попавшуюся валидную позицию для робота и 
 * запускаем оттуда поиск в ширину (BFS). Робот может двигаться в 4-х направлениях, 
 * переходя только по валидным позициям. Все позиции, до которых он смог доехать, 
 * мы помечаем как "посещенные" (visited).
 * * 4. ПРОВЕРКА ПОКРЫТИЯ (Снова 2D Префиксные суммы!)
 * Нам нужно убедиться, что множество всех посещенных роботом позиций ПОКРЫВАЕТ 
 * абсолютно все пустые клетки в комнате.
 * Клетка (r, c) считается покрытой, если робот побывал хотя бы в одной стартовой 
 * позиции, которая накрывает (r, c). Левый верхний угол такого робота должен 
 * находиться в пределах от (r - S + 1, c - S + 1) до (r, c).
 * Чтобы быстро проверить, есть ли хотя бы одна посещенная позиция в этом окошке, 
 * мы строим двумерные префиксные суммы уже поверх матрицы visited!
 * * 5. ЭФФЕКТИВНОСТЬ И ПАМЯТЬ
 * Так как N и M могут быть до 5*10^6, создавать классические vector<vector<int>> 
 * слишком медленно (аллокация + кэш-промахи). Мы разворачиваем 2D матрицы в 1D 
 * массивы и используем формулу индекса [r * M + c]. Память выделяется ровно 1 раз 
 * при старте программы, что делает код невероятно быстрым!
 * P.S. Решение получает Accepted и трати максимум 2.229s (время) и 117.81Mb (память).
 * =======================================================================================
 */

#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// Глобальные переменные. Выделяем память один раз, чтобы сберечь драгоценное время!
int n, m, k;
vector<int> obs;         // 1, если в клетке препятствие, 0 - пусто
vector<int> pref_obs;    // 2D префиксные суммы для препятствий
vector<char> valid_pos;  // 1, если позиция левого верхнего угла валидна для робота размера S
vector<char> visited;    // 1, если робот смог доехать до этой стартовой позиции
vector<int> pref_vis;    // 2D префиксные суммы по массиву visited
vector<pair<int, int>> q;// Очередь для нашего BFS (поиск в ширину)

// Функция для получения суммы препятствий в прямоугольнике [r1..r2]x[c1..c2]
inline int get_obs_sum(int r1, int c1, int r2, int c2) {
    if (r1 > r2 || c1 > c2) return 0;
    int M_plus_1 = m + 1;
    return pref_obs[(r2 + 1) * M_plus_1 + (c2 + 1)]
         - pref_obs[r1 * M_plus_1 + (c2 + 1)]
         - pref_obs[(r2 + 1) * M_plus_1 + c1]
         + pref_obs[r1 * M_plus_1 + c1];
}

// Главная проверяющая функция: справится ли робот размера S со всей комнатой?
bool check(int S) {
    int start_r = -1, start_c = -1;

    // Быстро очищаем массивы перед новой проверкой
    fill(valid_pos.begin(), valid_pos.end(), 0);
    fill(visited.begin(), visited.end(), 0);

    // 1. Ищем все валидные стартовые позиции для робота
    for (int r = 0; r <= n - S; ++r) {
        for (int c = 0; c <= m - S; ++c) {
            if (get_obs_sum(r, c, r + S - 1, c + S - 1) == 0) {
                valid_pos[r * m + c] = 1;
                // Запоминаем первую попавшуюся позицию для старта BFS
                if (start_r == -1) {
                    start_r = r;
                    start_c = c;
                }
            }
        }
    }

    // Если нет вообще ни одной позиции, куда поместится робот
    if (start_r == -1) return false;

    // 2. Запускаем BFS, чтобы найти все достижимые позиции из стартовой
    q.clear();
    q.push_back({start_r, start_c});
    visited[start_r * m + start_c] = 1;

    int head = 0;
    int dr[] = {-1, 1, 0, 0};
    int dc[] = {0, 0, -1, 1};

    while (head < (int)q.size()) {
        auto [r, c] = q[head++];

        for (int i = 0; i < 4; ++i) {
            int nr = r + dr[i];
            int nc = c + dc[i];

            // Если не вышли за границы и клетка безопасна
            if (nr >= 0 && nr <= n - S && nc >= 0 && nc <= m - S) {
                if (valid_pos[nr * m + nc] && !visited[nr * m + nc]) {
                    visited[nr * m + nc] = 1;
                    q.push_back({nr, nc});
                }
            }
        }
    }

    // 3. Строим префиксные суммы по посещенным позициям
    fill(pref_vis.begin(), pref_vis.end(), 0);
    int M_plus_1 = m + 1;
    for (int r = 0; r < n; ++r) {
        int r_idx = r * M_plus_1;
        int nr_idx = (r + 1) * M_plus_1;
        int r_m = r * m;
        for (int c = 0; c < m; ++c) {
            pref_vis[nr_idx + c + 1] = visited[r_m + c]
                                     + pref_vis[r_idx + c + 1]
                                     + pref_vis[nr_idx + c]
                                     - pref_vis[r_idx + c];
        }
    }

    // Лямбда-функция: проверяет, есть ли хотя бы одна достигнутая позиция в окошке
    auto is_covered = [&](int r1, int c1, int r2, int c2) {
        if (r1 > r2 || c1 > c2) return false;
        int sum = pref_vis[(r2 + 1) * M_plus_1 + (c2 + 1)]
                - pref_vis[r1 * M_plus_1 + (c2 + 1)]
                - pref_vis[(r2 + 1) * M_plus_1 + c1]
                + pref_vis[r1 * M_plus_1 + c1];
        return sum > 0;
    };

    // 4. Проверяем каждую пустую клетку в комнате
    for (int r = 0; r < n; ++r) {
        for (int c = 0; c < m; ++c) {
            if (obs[r * m + c] == 0) { // Если клетка не занята препятствием
                // Диапазон возможных левых верхних углов робота, которые накроют клетку (r, c)
                int r1 = max(0, r - S + 1);
                int c1 = max(0, c - S + 1);
                int r2 = min(n - S, r);
                int c2 = min(m - S, c);

                // Если клетку невозможно покрыть или робот до туда не доезжает
                if (!is_covered(r1, c1, r2, c2)) {
                    return false;
                }
            }
        }
    }

    // Ура, робот успешно всё помыл! 🧽
    return true;
}

int main() {
    // Ускоряем ввод-вывод для C++
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    if (!(cin >> n >> m >> k)) return 0;

    // Выделяем память один раз под максимальные размеры
    obs.assign(n * m, 0);
    pref_obs.assign((n + 1) * (m + 1), 0);
    valid_pos.assign(n * m, 0);
    visited.assign(n * m, 0);
    pref_vis.assign((n + 1) * (m + 1), 0);
    q.reserve(n * m);

    // Считываем препятствия (переводим в 0-индексацию для удобства)
    for (int i = 0; i < k; ++i) {
        int r, c;
        cin >> r >> c;
        --r; --c;
        obs[r * m + c] = 1;
    }

    // Заполняем массив префиксных сумм для препятствий (он нужен на всё время работы)
    int M_plus_1 = m + 1;
    for (int r = 0; r < n; ++r) {
        for (int c = 0; c < m; ++c) {
            pref_obs[(r + 1) * M_plus_1 + (c + 1)] = obs[r * m + c]
                + pref_obs[r * M_plus_1 + (c + 1)]
                + pref_obs[(r + 1) * M_plus_1 + c]
                - pref_obs[r * M_plus_1 + c];
        }
    }

    // Если даже минимальный робот (1x1) не может убрать всю доступную площадь 
    // (например, пустые клетки разделены стеной), значит никто не справится.
    if (!check(1)) {
        cout << -1 << "\n";
        return 0;
    }

    // Бинарный поиск ответа
    int low = 1, high = min(n, m);
    int ans = -1;

    while (low <= high) {
        int mid = low + (high - low) / 2;
        
        if (check(mid)) {
            ans = mid;      // Запоминаем успешный размер
            low = mid + 1;  // Пробуем взять робота еще побольше
        } else {
            high = mid - 1; // Робот слишком большой, уменьшаем
        }
    }

    cout << ans << "\n";
    return 0;
}
