#include <iostream>
#include <vector>
#include <algorithm>
#include <iomanip>

using namespace std;

/*
 * Эта задача — настоящая жемчужина вычислительной геометрии. Она кажется сложной
 * из-за функции max, которая делает выражение для суммы расстояний «негладким».
 * Но трюк с поворотом осей на 45° превращает её в элементарную задачу на поиск медианы.
 *
 * === МАТЕМАТИЧЕСКОЕ ОБОСНОВАНИЕ ===
 * Главный секрет заключается в тождестве:
 *     max(|A|, |B|) = (|A + B| + |A - B|) / 2
 *
 * Применим это к расстоянию Чебышёва между точками P(x, y) и Pi(xi, yi):
 *     ρ∞(P, Pi) = max(|x - xi|, |y - yi|) = (|(x - xi) + (y - yi)| + |(x - xi) - (y - yi)|) / 2
 *
 * Перегруппируем слагаемые:
 *     ρ∞(P, Pi) = (|(x + y) - (xi + yi)| + |(x - y) - (xi - yi)|) / 2
 *
 * Теперь введем новые координаты (замена переменных):
 *   •  u = x + y,     v = x - y
 *   •  ui = xi + yi,  vi = xi - yi
 *
 * Тогда суммарное расстояние S превращается в:
 *     S = Σ [ (|u - ui| + |v - vi|) / 2 ] = 1/2 * ( Σ |u - ui| + Σ |v - vi| )
 *
 * Теперь получаем:
 * 
 * 1. Переменные u и v стали независимыми. Мы можем минимизировать сумму по u и 
 *    по v по отдельности.
 * 
 * 2. Задача минимизации Σ|u - ui| — это классика. Сумма абсолютных отклонений 
 *    минимальна, когда u является медианой набора точек ui.
 * 
 * 3. Найдя оптимальные u и v, мы легко возвращаемся к исходным координатам:
 *    •  x = (u + v) / 2
 *    •  y = (u - v) / 2
 *
 * === ГЕОМЕТРИЧЕСКАЯ ИНТУИЦИЯ ===
 * Геометрически замена координат с (x,y) на (u,v) = (x+y, x-y) эквивалентна повороту осей координат на 45°
 * (и растяжению в сколько-то корней из 2 раз). При этом единичная окружность в метрике Чебышёва (= множество точек, находящихся
 * на расстоянии 1 Чебышёва от (0,0)) из квадрата (так он выглядит в (x,y)) превращается в ромб (в координатах (u,v)),
 * а это уже является окружностью в манхэттенской метрике, которую минимизируем легко как описано выше.
 */


int main() {
    // Настройка точности вывода
    cout << fixed << setprecision(10);

    int n;
    if (!(cin >> n)) return 0;

    vector<double> x(n), y(n);
    vector<double> u(n), v(n);

    for (int i = 0; i < n; ++i) {
        cin >> x[i] >> y[i];
        // Преобразование координат
        u[i] = x[i] + y[i];
        v[i] = x[i] - y[i];
    }

    // Для минимизации суммы |u - u_i| нужно выбрать u равным медиане.
    // Сортируем массивы для поиска медианы.
    sort(u.begin(), u.end());
    sort(v.begin(), v.end());

    // Выбираем медиану. Если n нечетное — это центральный элемент.
    // Если четное — подойдет любое число на отрезке между двумя центральными.
    double target_u = u[n / 2];
    double target_v = v[n / 2];

    // Вычисляем минимальную сумму расстояний в новых координатах
    double sum_u = 0, sum_v = 0;
    for (int i = 0; i < n; ++i) {
        sum_u += abs(target_u - u[i]);
        sum_v += abs(target_v - v[i]);
    }

    // Итоговая минимальная сумма Чебышёва (не забываем деление на 2 из формулы)
    double min_total_dist = (sum_u + sum_v) / 2.0;

    // Переход обратно к исходным координатам центра
    double res_x = (target_u + target_v) / 2.0;
    double res_y = (target_u - target_v) / 2.0;

    // Вывод результата
    cout << min_total_dist << endl;
    cout << res_x << " " << res_y << endl;

    return 0;
}
