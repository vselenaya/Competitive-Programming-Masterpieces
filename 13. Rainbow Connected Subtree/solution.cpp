#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

/*
 * =================================================================================================
 * ИДЕЯ РЕШЕНИЯ (Деревья + 2-SAT)
 * =================================================================================================
 * 0. Заметим, что раз в деревне 2n домов (=вершины графа) и 2n-1 дорога (= рёбра графа), а добраться
 * можно между любыми двум домами (= граф связен), то деревня образует дерево (так как дерево на
 * l вершинах имеет ровно l-1 ребро).
 *
 * 1. Переформулировка связности:
 * Главная сложность: как выразить условие "выбранные n вершин образуют связное поддерево"?
 * Ключевой инсайт: если мы зафиксируем какую-то вершину `r`, которая ТОЧНО входит в наш ответ,
 * и подвесим дерево за нее, то условие связности превращается в очень простое локальное правило:
 * "Если мы берем вершину v в ответ, то мы ОБЯЗАНЫ взять и ее родителя p_v".
 *
 * 2. Сведение к 2-SAT:
 * Заведем для каждой вершины дерева булеву переменную b_v (true - берем вершину, false - не берем).
 * У нас есть два типа условий:
 * А. Из каждой пары вершин одного цвета (u и v) нужно взять ровно одну.
 * Это значит (b_u || b_v) И (!b_u || !b_v).
 * Б. Условие связности: если взяли v, берем p_v (b_v -> b_{p_v}).
 * По правилам логики импликация a -> b эквивалентна (!a || b). Значит: (!b_v || b_{p_v}).
 * Все условия представлены в виде дизъюнкций (ИЛИ) с двумя аргументами. Это классический 2-SAT!
 *
 * 3. Как выбрать корень `r`?
 * Ответ должен содержать ровно одну вершину каждого цвета. Возьмем любой цвет, например, цвет 1.
 * В дереве есть ровно две вершины этого цвета. Хотя бы одна из них (а точнее ровно одна)
 * ОКАЖЕТСЯ в итоговом ответе. 
 * Значит, мы можем просто дважды запустить наш 2-SAT:
 * - Сначала пробуем первую вершину цвета 1 в качестве корня `r`.
 * - Если не вышло, пробуем вторую вершину цвета 1 в качестве корня `r`.
 * Если оба варианта неразрешимы, то ответа не существует.
 * =================================================================================================
 */

// Структура для 2-SAT, чтобы удобно было очищать графы между test cases
struct TwoSat {
    int n;  // количество переменных
    vector<vector<int>> g, gr;  // граф и обратный граф (их списки смежности), а также другие структуры для решения задачи 2-SAT
    vector<bool> used;          // (решается она топологической сортировкой графа импликаций, которые выводятся из условий 2-SAT...)
    vector<int> order, component;

    TwoSat(int vars) : n(vars) {
        // У нас n переменных, для каждой нужна обычная и инвертированная вершина
        g.assign(2 * n, vector<int>());
        gr.assign(2 * n, vector<int>());
    }

    // Добавить условие (x || y)
    // Сами x и y передаются уже как 2*i (для x_i) или 2*i+1 (для !x_i), где i - индекс переменной (от 0 до n-1)
    void add_disjunction(int x, int y) {
        g[x ^ 1].push_back(y);
        g[y ^ 1].push_back(x);
        gr[y].push_back(x ^ 1);
        gr[x].push_back(y ^ 1);
    }

    void dfs1(int v) {  // Топологическая сортировка
        used[v] = true;
        for (int u : g[v]) {
            if (!used[u]) dfs1(u);
        }
        order.push_back(v);
    }

    void dfs2(int v, int c) {  // Выделение компонент (алгоритм Косарайю): component[v] тем БОЛЬШЕ, чем РАНЬШЕ она в топсорте
        component[v] = c;
        for (int u : gr[v]) {
            if (component[u] == -1) dfs2(u, c);
        }
    }

    // Возвращает вектор значений (true/false) или пустой вектор, если 2-SAT не разрешим (есть противоречие)
    vector<bool> solve() {
        used.assign(2 * n, false);
        order.clear();
        for (int i = 0; i < 2 * n; ++i) {
            if (!used[i]) dfs1(i);
        }

        reverse(order.begin(), order.end());

        component.assign(2 * n, -1);
        int c = 0;
        for (int v : order) {
            if (component[v] == -1) dfs2(v, c++);
        }

        vector<bool> res(n);
        for (int i = 0; i < n; ++i) {
            // Если x_i и !x_i в одной компоненте сильной связности - это противоречие
            if (component[2 * i] == component[2 * i + 1]) {
                return {};
            }
            // Переменная истинна (x_i = true), если ее компонента топологически позже компоненты ее отрицания (иначе x_i = false)
            res[i] = component[2 * i] > component[2 * i + 1];
        }
        return res;  // res[i] хранит значение x_i (true или false)
    }
};

// Функция решения одного теста
void solve() {
    int n;
    cin >> n;

    int num_vertices = 2 * n;
    vector<int> color(num_vertices);
    vector<vector<int>> color_nodes(n + 1);  // Храним списки вершин для каждого цвета
    
    for (int i = 0; i < num_vertices; ++i) {
        cin >> color[i];
        color_nodes[color[i]].push_back(i);
    }

    vector<vector<int>> adj(num_vertices);  // Список смежности графа-деревни
    for (int i = 0; i < num_vertices - 1; ++i) {
        int u, v;
        cin >> u >> v;
        --u; --v;  // Переходим к 0-индексации
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    // Функция-помощник: пытается найти ответ, считая вершину `root` корнем дерева
    auto try_root = [&](int root) -> vector<bool> {
        vector<int> parent(num_vertices, -1);
        
        // Запускаем лямбда-DFS для поиска предков в графе, подвешенном за root:
        auto dfs_tree = [&](auto& self, int v, int p) -> void {
            parent[v] = p;
            for (int u : adj[v]) {
                if (u != p) self(self, u, v);
            }
        };
        dfs_tree(dfs_tree, root, -1);

        // Инициализируем 2-SAT для 2*n переменных
        TwoSat ts(num_vertices);

        // 1. Условие на цвета: ровно одна вершина каждого цвета
        for (int c = 1; c <= n; ++c) {
            int u = color_nodes[c][0];
            int v = color_nodes[c][1];
            // (b_u || b_v)
            ts.add_disjunction(2 * u, 2 * v);
            // (!b_u || !b_v)
            ts.add_disjunction(2 * u + 1, 2 * v + 1);
        }

        // 2. Условие связности: если берем вершину, то обязаны взять ее родителя
        for (int i = 0; i < num_vertices; ++i) {
            if (i != root) {
                int p = parent[i];
                // !b_i || b_p
                ts.add_disjunction(2 * i + 1, 2 * p);
            }
        }

        // Форсируем корень: мы предполагаем, что root в ответе (иначе зачем он корень?)
        // b_root || b_root
        ts.add_disjunction(2 * root, 2 * root);

        return ts.solve();
    };

    // Берем две вершины цвета 1 (по условию цвета от 1 до n)
    int root1 = color_nodes[1][0];
    int root2 = color_nodes[1][1];

    vector<bool> ans = try_root(root1);
    if (ans.empty()) {
        ans = try_root(root2);  // Если первая не подошла, пробуем вторую
    }

    if (ans.empty()) {  // Если и вторая не подходит, решения нет
        cout << -1 << "\n";
    } else {
        // Выводим те вершины, для которых переменная оказалась true
        for (int i = 0; i < num_vertices; ++i) {
            if (ans[i]) {
                cout << i + 1 << " ";  // Возвращаем 1-индексацию, делая +1
            }
        }
        cout << "\n";
    }

    return;
}

int main() {
    // Ускоряем ввод-вывод
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}
