#include <iostream>
#include <vector>
#include <cmath>
#include <algorithm>

/*
 * =================================================================================================
 * ОПТИМИЗИРОВАННОЕ РЕШЕНИЕ: ЛИНЕЙНЫЕ УРАВНЕНИЯ + МЕДИАНА
 * =================================================================================================
 * * ИДЕЯ:
 * Подвесим граф за вершину 1 и скажем: пусть x_1 = X (где X — пока неизвестная переменная).
 * Тогда для любой вершины v её значение x_v можно выразить через X в виде линейного уравнения:
 *                      x_v = coeff[v] * X + k[v], где coeff может быть только 1 или -1.
 * Действительно, так можно, ведь:
 * - Для корня (вершина 1): coeff = 1, k = 0  => (x_1 = 1*X + 0).
 * - Для соседа u вершины v: x_v = c - x_u = c - (coeff[u] * X + k_u) = (-coeff[u]) * X + (c - k_u). Снова
 *   выражение того же вида, но коэффициент поменял знак -> аналогично распространим на все вершины и получим,
 *   что значение в каждой так линейно выражается, а коэффициент меняет знак при переходе по ребру.
 *
 * ОБРАБОТКА ЦИКЛОВ В DFS:
 * Когда мы встречаем ребро в уже посещенную вершину (u -> v), получаем уравнение:
 * (coeff[u] * X + k[u]) + (coeff[v] * X + k[v]) = weight
 * 1. Если coeff[u] == coeff[v]: это НЕЧЕТНЫЙ цикл (в нём нечётное количество рёбер). Уравнение превращается в:
 *                      2 * coeff[u] * X = weight - k[u] - k[v]. Отсюда X вычисляется ОДНОЗНАЧНО.
 * 2. Если coeff[u] != coeff[v]: это ЧЕТНЫЙ цикл. X сокращается: k[u] + k[v] = weight.
 *                      Если это равенство неверно, решения не существует (No).
 *
 * МИНИМИЗАЦИЯ СУММЫ (Геометрическая интерпретация):
 * Если X не зафиксирован нечетным циклом, нам нужно минимизировать Σ |coeff[i] * X + k[i]|.
 * Так как |coeff| всегда 1, это выражение эквивалентно Σ |X - (-coeff[i] * k[i])|.
 * Это классическая задача: "Найти точку на прямой, сумма расстояний от которой до набора точек минимальна".
 * Ответ — МЕДИАНА набора точек {-coeff[i] * k[i]}.
 *
 * ПРЕИМУЩЕСТВА:
 * - Работает быстрее тернарного поиска.
 * - Нет проблем с точностью или границами поиска (не нужно ставить 1e11).
 * - Код получается чище и короче.
 * - Код проходит все тесты с максимальным потреблением: 124ms (время) и 18.69Mb (память)
 * =================================================================================================
 */

using namespace std;

// Структура для ребра
struct Edge {
    int to;
    long long w;
};

// Глобальные переменные (в задачах на графы так удобнее, чтобы не таскать в аргументах)
vector<vector<Edge>> adj;
vector<bool> vis;

// Массивы для уравнения x_v = coef[v] * X + k[v]
vector<int> coef;
vector<long long> k;

// Переменные для фиксации X (если найдем нечётный цикл)
bool x_fixed = false;
long long fixed_X = 0;
bool possible = true;

// Главный DFS, который строит уравнения и проверяет циклы
void dfs(int v) {
    for (const auto& edge : adj[v]) {
        int u = edge.to;
        long long w = edge.w;

        if (!vis[u]) {
            // Вершина не посещена: вычисляем её уравнение
            // x_u = w - x_v 
            // x_u = w - (coef[v] * X + k[v]) = (-coef[v]) * X + (w - k[v])
            coef[u] = -coef[v];
            k[u] = w - k[v];
            vis[u] = true;
            dfs(u);
        } else {
            // Вершина уже посещена: проверяем согласованность ребра
            // Требуется: x_u + x_v = w
            // coef[u]*X + k[u] + coef[v]*X + k[v] = w
            
            if (coef[u] != coef[v]) {
                // X сокращается (чётный цикл)
                // Должно выполняться: k[u] + k[v] == w
                if (k[u] + k[v] != w) {
                    possible = false;
                }
            } else {
                // coef[u] == coef[v] (нечётный цикл)
                // 2 * coef[v] * X + k[u] + k[v] = w
                // 2 * coef[v] * X = w - k[u] - k[v]
                long long diff = w - k[u] - k[v];
                
                // Проверяем, делится ли нацело (уровни сигнала целые)
                if (diff % 2 != 0) {
                    possible = false;  // если нет -- решения тоже нет!
                } else {
                    // Вычисляем конкретный X
                    long long candidate_X = diff / (2 * coef[v]);
                    
                    // Если X еще не был зафиксирован другим циклом, фиксируем
                    if (!x_fixed) {
                        x_fixed = true;
                        fixed_X = candidate_X;
                    } 
                    // Если уже зафиксирован, проверяем на противоречие
                    else if (fixed_X != candidate_X) {
                        possible = false;
                    }
                }
            }
        }
    }
}

int main() {
    // Ускорение ввода-вывода
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    cin >> n >> m;

    adj.resize(n + 1);
    vis.assign(n + 1, false);
    coef.resize(n + 1);
    k.resize(n + 1);

    for (int i = 0; i < m; ++i) {
        int u, v;
        long long w;
        cin >> u >> v >> w;
        adj[u].push_back({v, w});
        adj[v].push_back({u, w});
    }

    // Инициализируем корень (вершину 1)
    // x_1 = 1 * X + 0
    coef[1] = 1;
    k[1] = 0;
    vis[1] = true;

    // Запускаем обход и строим зависимости
    dfs(1);

    if (!possible) {
        cout << "No\n";
        return 0;
    }

    long long best_X = 0;

    if (x_fixed) {
        // Если нечётный цикл жестко задал X, у нас нет выбора
        best_X = fixed_X;
    } else {
        // Граф двудольный. Ищем X, минимизирующий сумму |coef[i] * X + k[i]|
        // Что эквивалентно минимизации |X - (-coef[i] * k[i])|
        vector<long long> values;
        for (int i = 1; i <= n; ++i) {
            values.push_back(-coef[i] * k[i]);
        }
        
        // Сортируем и берем медиану
        sort(values.begin(), values.end());
        best_X = values[n / 2];  // это элемент посередине (как для нечётного n, так и для чётного -- там любой между сердними двумя подходит)
    }

    // Финальная проверка всех рёбер для надежности
    // И вычисление финальных значений
    vector<long long> ans(n + 1);
    for (int i = 1; i <= n; ++i) {
        ans[i] = coef[i] * best_X + k[i];
    }

    for (int u = 1; u <= n; ++u) {
        for (const auto& edge : adj[u]) {
            int v = edge.to;
            if (ans[u] + ans[v] != edge.w) {
                cout << "No\n";
                return 0;
            }
        }
    }

    // Если всё хорошо, выводим ответ
    cout << "Yes\n";
    for (int i = 1; i <= n; ++i) {
        cout << ans[i] << (i == n ? "" : " ");
    }
    cout << "\n";

    return 0;
}
