/**
 * ИДЕЯ РЕШЕНИЯ:
 * Нам нужно найти N, зная только, является ли текущее число X квадратом.
 * Ключевая формула: (x + 1)² - x² = 2x + 1.
 * Расстояние между соседними квадратами всегда НЕЧЕТНОЕ и зависит от x линейно.
 * * АЛГОРИТМ:
 * 1. Находим ближайший квадрат: Прибавляем по 1 (суммарно d1), пока не получим ответ "1".
 * Теперь X = N + d1 = x² (где x — какое-то целое число).
 * * 2. Переход к следующему квадрату (x+1)²: 
 * Расстояние до него (2x + 1) нечетное. Чтобы сэкономить запросы, мы хотим 
 * шагать по 2. Но x² и (x+1)² всегда разной четности. Если мы будем 
 * просто прибавлять по 2, мы никогда не попадем ровно в (x+1)².
 * Поэтому мы делаем один "нечетный" сдвиг (в блоке if-else), чтобы 
 * совместить четность текущего X с четностью будущего (x+1)².
 * * 3. Поиск (x+1)²: Прибавляем по 2, пока снова не увидим "1".
 * Теперь X = N + d2 = (x + 1)².
 * * 4. Вычисление:
 * (N + d2) - (N + d1) = (x + 1)² - x²
 * d2 - d1 = 2x + 1  =>  x = (d2 - d1 - 1) / 2
 * N = x² - d1
 * * ОГРАНИЧЕНИЯ:
 * - Запросы: ~1000 на поиск первого квадрата + ~1000 (шагами по 2) на второй.
 * В сумме около 2000, что вписывается в лимит 2023.
 * - Регистр X: N <= 10^6, расстояние до квадратов небольшое, X не превысит 2^20.
 */

#include <iostream> 
#include <cmath>
#include <vector>  
#include <string> 
#include <algorithm> 
#include <utility>  
#include <unordered_map>

using namespace std;

    
int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    std::cout.tie(0);
    
    int d1 = 0, d2 = 0;
    bool res = 0;
    while (res != 1) {
        cout << "? 1" << endl;
        d1 += 1;
        cin >> res;
    }
    d2 = d1;
    
    if (d1 % 2 == 1) {
        cout << "? " << d1 << endl;
        d2 += d1;
        cin >> res;
    } else {
        cout << "? 1" << endl;
        cin >> res;
        cout << "? " << d1 << endl;
        cin >> res;
        d2 += d1 + 1;
    }
    
    while (res != 1) {
        cout << "? 2" << endl;
        cin >> res;
        d2 += 2;
    }
    
    int x = (d2 - d1 - 1) / 2;
    int N = x * x - d1;
    
    cout << "! " << N << endl;
    
    



    return 0;
}
